import UIKit

enum SheetHeight {
    static let large = Screen.height * 3/4
    static let medium = Screen.height / 2.0 + 50.0
    static let low = Screen.height / 3.0 + 10.0
    static let tiny = Screen.height / 4.0 + 10.0
}
